    echo "‚ùå pnpm –Ω–µ –Ω–∞–π–¥–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ pnpm: npm install -g pnpm"
    exit 1
fi

echo "‚úÖ pnpm $(pnpm -v) –Ω–∞–π–¥–µ–Ω"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è..."

if [ ! -f .env.local ]; then
    echo "‚ö†Ô∏è  –§–∞–π–ª .env.local –Ω–µ –Ω–∞–π–¥–µ–Ω. –°–æ–∑–¥–∞—é —à–∞–±–ª–æ–Ω..."
    cp .env.example .env.local 2>/dev/null || echo "–°–æ–∑–¥–∞–π—Ç–µ —Ñ–∞–π–ª .env.local —Å –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–º–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏"
